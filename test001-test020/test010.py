# Python 连接符详细操作

print('=================================')

age = 25
text = "我今年 " + str(age) + " 岁。"
print(text)  # 输出：我今年 25 岁。
#使用 str() 转换整数为字符串，再用 + 连接

print('=================================')

age = 25
text = f"我今年 {age} 岁。"
print(text)  # 输出：我今年 25 岁。
#使用 f-string（Python 3.6+）
print('=================================')

age = 25
text = "我今年 {} 岁。".format(age)
print(text)  # 输出：我今年 25 岁。
#使用 format() 方法

print('=================================')

age = 25
text = "我今年 %d 岁。" % age
print(text)  # 输出：我今年 25 岁。
#使用 % 格式化（传统方式）

print('=================================')

age = 25
print("我今年", age, "岁。")  # 输出：我今年 25 岁。
#在 print() 函数中用逗号分隔（自动转换类型，但添加空格）

print('=================================')

